SELECT S.ASSET_NUM
FROM   S_PARAM P, 
       S_QUOTE_ITEM I,
       S_QUOTE_SOLN S
WHERE I.QUOTE_SOLN_ID = S.ROW_ID
AND   P.REF_TABLE_ID = I.ROW_ID
AND   P.X_RANGE_INICIAL IS NOT NULL 
AND   P.X_RANGE_FINAL IS NOT NULL 
AND   SUBSTR(TRIM(P.X_RANGE_INICIAL),1,2) = SUBSTR(TRIM(P.X_RANGE_FINAL),1,2) 
AND   ? BETWEEN trim(P.X_RANGE_INICIAL) AND trim(P.X_RANGE_FINAL)  
      and stat_cd = 'Ativo'   -- INCLUIDO POR DANTE
AND COPIED_FLG = 'N' 
ORDER BY S.CREATED DESC