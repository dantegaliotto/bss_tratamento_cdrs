SELECT SA1.PROD_ID row_id,
       SPI.NAME name,
       NVL (SA_PLANO.X_ACTIVATION_DT, SA_PLANO.X_INSTALL_DT) eff_end_dt
  FROM SIEBEL.S_ASSET SA,
       SIEBEL.S_ASSET SA1,
       SIEBEL.S_ASSET SA_PLANO,
       SIEBEL.S_PROD_INT SPI, 
       SIEBEL.S_PROD_INT SPI_OFER
 WHERE SA.OWNER_ACCNT_ID = SA1.OWNER_ACCNT_ID
   AND SA.SERIAL_NUM = ?
   AND SA.SERIAL_NUM not like 'TV%'
   AND SA1.PROD_ID = '1-7GLW'
   AND SA1.ROOT_ASSET_ID = SA_PLANO.ROOT_ASSET_ID
   AND SA_PLANO.PROD_ID = SPI.ROW_ID
   AND SA1.ROW_ID <> SA_PLANO.ROW_ID
   --AND SPI.TYPE = 'Plano Local'
   AND SA_PLANO.STATUS_CD = 'Ativo'
   AND SA.STATUS_CD = 'Ativo'
   AND SA1.STATUS_CD = 'Ativo'
   AND SA.PROM_INTEG_ID = SA1.PROM_INTEG_ID
   AND SA.PROMOTION_ID = SPI_OFER.ROW_ID
   AND SPI.NAME like '25 Ilimitado%'