SELECT s.row_id RPON, S.ASSET_NUM DESIGNADOR, I.*
FROM   S_PARAM P,
       S_QUOTE_ITEM I,
       S_QUOTE_SOLN S
WHERE I.QUOTE_SOLN_ID = S.ROW_ID
AND   P.REF_TABLE_ID = I.ROW_ID
AND   P.X_RANGE_INICIAL IS NOT NULL 
AND   P.X_RANGE_FINAL IS NOT NULL 
AND   SUBSTR(TRIM(P.X_RANGE_INICIAL),0,4) = SUBSTR(TRIM(P.X_RANGE_FINAL),0,4) 
AND   ? BETWEEN P.X_RANGE_INICIAL AND  P.X_RANGE_FINAL   
AND   I.PROD_ID <> '1-1BPLSY'
AND COPIED_FLG = 'N' 
AND S.STATUS_CD = 'Ativo'
AND I.STAT_CD <> 'Inativo'
ORDER BY S.CREATED DESC